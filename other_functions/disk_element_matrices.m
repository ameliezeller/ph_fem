    k11 = 4*L_y/L_x+2*(1-nu)*L_x/L_y;
    k33 = k11; k55 = k11; k77 = k11;
    k22 = 4*L_x/L_y+2*(1-nu)*L_y/L_x;
    k44 = k22; k66 = k22; k88 = k22;
    k12 = 3/2*(1+nu);
    k47 = k12; k38 = k12; k56 = k12;
    k13 = -4*L_y/L_x + (1-nu)*L_x/L_y;
    k57 = k13;
    k14 = -3/2*(1-3*nu);
    k27 = k14; k58 = k14; k36 = k14;
    k15 = -2*L_y/L_x - (1-nu)*L_x/L_y;
    k37 = k15;
    k16 = -3/2*(1+nu);
    k25 = k16; k78 = k16; k34 = k16;
    k17 = 2*L_y/L_x-2*(1-nu)*L_x/L_y;
    k35 = k17;
    k18 = 3/2*(1-3*nu);
    k23 = k18; k67 = k18; k45 = k18;
    k24 = 2*L_x/L_y-2*(1-nu)*L_y/L_x;
    k68 = k24;
    k26 = -2*L_x/L_y-(1-nu)*L_y/L_x;
    k48 = k26;
    k28 = -4*L_x/L_y+(1-nu)*L_y/L_x;
    k46 = k28;
    
    K = [k11 k12 k13 k14 k15 k16 k17 k18; ...
         k12 k22 k23 k24 k25 k26 k27 k28; ...
         k13 k23 k33 k34 k35 k36 k37 k38; ...
         k14 k24 k34 k44 k45 k46 k47 k48; ... 
         k15 k25 k35 k45 k55 k56 k57 k58; ...
         k16 k26 k36 k46 k56 k66 k67 k68; ...
         k17 k27 k37 k47 k57 k67 k77 k78; ...
         k18 k28 k38 k48 k58 k68 k78 k88].*(E*h/(12*(1 - nu^2)));
     
     M = [4 0 2 0 1 0 2 0; ...
          0 4 0 2 0 1 0 2; ... 
          2 0 4 0 2 0 1 0; ...
          0 2 0 4 0 2 0 1; ... 
          1 0 2 0 4 0 2 0; ...
          0 1 0 2 0 4 0 2; ...
          2 0 1 0 2 0 4 0; ... 
          0 2 0 1 0 2 0 4].*(rho*h*L_x*L_y/36);
end